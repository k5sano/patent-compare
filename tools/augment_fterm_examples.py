def apply_examples(path: Path, examples_map: dict):
    d = json.load(open(path, encoding="utf-8"))
    cats = d.get("categories", {})
    added = 0
    missing = []
    for code, exs in examples_map.items():
        axis = code[:2]
        if axis not in cats:
            missing.append(code)
            continue
        entries = cats[axis].get("entries", {})
        if code not in entries:
            missing.append(code)
            continue
        # 既存 examples に追加 (重複除去)
        cur = entries[code].get("examples") or []
        seen = set(cur)
        for e in exs:
            if e not in seen:
                cur.append(e)
                seen.add(e)
                added += 1
        entries[code]["examples"] = cur
    path.write_text(json.dumps(d, ensure_ascii=False, indent=2), encoding="utf-8")
    return added, missing


