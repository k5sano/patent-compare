# ---------------------------------------------------------------------------
# 2. 親子関係の自動構築
# ---------------------------------------------------------------------------

def build_tree(raw_nodes: list) -> tuple[dict, dict]:
    """
    RAW_NODESから nodes辞書 と reverse_index を構築して返す。

    Returns:
        nodes: {code: {label, definition, depth, parent, children, examples}}
        reverse_index: {example_str: [code, ...]}  (末端→親の順)
    """
    nodes: dict[str, dict] = {}

    # 第1パス: 全ノードを登録
    for code, depth, label, definition, examples in raw_nodes:
        nodes[code] = {
            "label": label,
            "definition": definition,
            "depth": depth,
            "parent": None,
            "children": [],
            "examples": list(examples),
        }

    # 第2パス: 親子リンクを設定
    # グループ内で「直前に現れた depth = 自分のdepth-1 のノード」を親とする。
    # グループは先頭2文字(AA/AB/AC/AD)で識別。depth=0はグループルートなので親なし。
    group_stack: dict[str, list[tuple[int, str]]] = {}  # group -> [(depth, code)]

    for code, depth, *_ in raw_nodes:
        group = code[:2]
        if group not in group_stack:
            group_stack[group] = []

        if depth == 0:
            # グループルート: スタックをリセット
            group_stack[group] = [(0, code)]
            continue

        # スタックから自分の親を探す: depth-1 のノード
        stack = group_stack[group]

        # スタックを現在のdepth以上のものをポップして整理
        while stack and stack[-1][0] >= depth:
            stack.pop()

        if stack:
            parent_code = stack[-1][1]
            nodes[code]["parent"] = parent_code
            nodes[parent_code]["children"].append(code)

        stack.append((depth, code))

    # 第3パス: reverse_index を構築
    # 各exampleについて、そのノード + 全祖先ノードのコードリストを登録
    # （末端→親の順: 最も具体的なコードが先頭）
    reverse_index: dict[str, list[str]] = {}

    def get_ancestors(code: str) -> list[str]:
        """code自身から根に向かうコードリスト（code自身を含む）"""
        result = []
        cur = code
        while cur:
            result.append(cur)
            cur = nodes[cur]["parent"]
        return result

    for code, depth, label, definition, examples in raw_nodes:
        for ex in examples:
            ex_clean = ex.strip()
            if not ex_clean:
                continue
            ancestors = get_ancestors(code)
            if ex_clean not in reverse_index:
                reverse_index[ex_clean] = ancestors
            else:
                # すでに登録済みの場合はマージ（重複排除、順序保持）
                existing = reverse_index[ex_clean]
                for a in ancestors:
                    if a not in existing:
                        existing.append(a)

    return nodes, reverse_index


# ---------------------------------------------------------------------------
# 3. 検証
# ---------------------------------------------------------------------------

def verify(nodes: dict, reverse_index: dict) -> list[str]:
    """整合性チェック。エラーメッセージのリストを返す（空なら問題なし）"""
    errors = []

    for code, info in nodes.items():
        # children の逆参照チェック
        for child in info["children"]:
            if child not in nodes:
                errors.append(f"[{code}] children に未定義コード: {child}")
            elif nodes[child]["parent"] != code:
                errors.append(
                    f"[{code}] children[{child}] の parent が不一致: "
                    f"{nodes[child]['parent']}"
                )
        # parent の逆参照チェック
        if info["parent"]:
            p = info["parent"]
            if p not in nodes:
                errors.append(f"[{code}] parent に未定義コード: {p}")
            elif code not in nodes[p]["children"]:
                errors.append(
                    f"[{code}] parent[{p}] の children にこのコードがない"
                )

    # reverse_index のコード存在チェック
    for ex, codes in reverse_index.items():
        for c in codes:
            if c not in nodes:
                errors.append(
                    f"reverse_index['{ex}'] に未定義コード: {c}"
                )

    return errors


# ---------------------------------------------------------------------------
# 4. 統計サマリ
# ---------------------------------------------------------------------------

def summary(nodes: dict, reverse_index: dict) -> str:
    total = len(nodes)
    roots = sum(1 for v in nodes.values() if v["depth"] == 0)
    leaves = sum(1 for v in nodes.values() if not v["children"])
    ex_total = sum(len(v["examples"]) for v in nodes.values())
    ri_total = len(reverse_index)
    max_depth = max(v["depth"] for v in nodes.values())

    return (
        f"  ノード総数    : {total}\n"
        f"  グループルート: {roots}\n"
        f"  末端ノード    : {leaves}\n"
        f"  最大深さ      : {max_depth}\n"
        f"  例示成分総数  : {ex_total}\n"
        f"  逆引きキー数  : {ri_total}"
    )


